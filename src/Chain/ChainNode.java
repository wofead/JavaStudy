package Chain;

public class ChainNode {
    private int cur;
    private int data;
    public void setCur(int data){
        this.cur = data;
    }

    public int getCur(){
        return this.cur;
    }

    public void setData(int data){
        this.data = data;
    }

    public int getData(){
        return this.data;
    }
}
